// Variables can hold different types of data. Here are some examples:
// This is called a string. It is a sequence of characters.
const name = 'Scott';
// This is a number
const age = 32;
// This is a float. It is a number with a decimal point.
const pi = 3.14;
// This is a boolean. It can be either true or false.
const isCool = true;
// This is an array. It is a list of values.
const pets = ['dog', 'cat', 'fish'];
// This is an object. It is a collection of key-value pairs.
// Each value in the object can be any type of data.
const person = {
  name: 'Scott',
  age: 32,
  isCool: true,
  pets: ['dog', 'cat', 'fish']
};

console.log(person);
